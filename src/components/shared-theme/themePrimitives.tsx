// Extensão da tipagem do Paper para permitir variantes customizadas, como 'highlighted'.
declare module '@mui/material/Paper' {
  interface PaperPropsVariantOverrides {
    highlighted: true; // Permite usar <Paper variant="highlighted" />
  }
}

// Extensão da tipagem do MUI para suportar paletas customizadas e tokens de design.
declare module '@mui/material/styles' {
  // Define um range de cores para facilitar a criação de paletas escaláveis e consistentes.
  interface ColorRange {
    50: string;
    100: string;
    200: string;
    300: string;
    400: string;
    500: string;
    600: string;
    700: string;
    800: string;
    900: string;
  }

  // Permite que PaletteColor herde o range de cores, facilitando uso de tokens customizados.
  interface PaletteColor extends ColorRange {}

  // Adiciona o token baseShadow à paleta, útil para customização global de sombras.
  interface Palette {
    baseShadow: string;
  }
}

